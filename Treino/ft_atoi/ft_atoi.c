int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	k = 1;
	while (str[i])
	{
		while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
			i++;
		while ((str[i] == '-' || str[i] == '+') && (str[i + 1] >= '0' && str[i + 1] <= '9'))
		{
			if (str[i] == '-')
				k = k * (-1);
			i++;
		}
		while (str[i] && str[i] >= '0' && str[i] <= '9')
		{
			j = j * 10 + str[i] - '0';
			i++;
		}
		return (j * k);
	}
}
